// Використовуючи конструкцію if..else, напишіть код, що запитує: 
// ‘Яка “офіційна” назва JavaScript?’ Якщо відвідувач вводить “ECMAScript”, виведіть “Правильно!”, 
// в іншому випадку — виведіть: “Ви не знаєте? ECMAScript!”.

let answer = "Яка офіційна назва JavaScript?'";

if (answer == 'ECMAScript') {
  alert('Правильно!');
} else {
  alert('Ви не знаєте? ECMAScript!');
}